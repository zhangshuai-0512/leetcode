class Solution {
public:
    int getdepth(TreeNode* node) {
        if (!node) return 0;
        int leftdepth = getdepth(node->left);       // å·¦
        int rightdepth = getdepth(node->right);     // å³
        int depth = 1 + max(leftdepth, rightdepth); // ä¸­
        return depth;
    }
    int maxDepth(TreeNode* root) {
        return getdepth(root);
    }
};
