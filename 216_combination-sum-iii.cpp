class Solution
{
public:
    vector<vector<int>> result;
    vector<int> path;
    int maxDepth;

    vector<vector<int>> combinationSum3(int k, int n)
    {
        maxDepth = k;
        backtracking(n, 0, 1);
        return result;
    }

    void backtracking(int tarsum, int cursum, int startindex)
    {
        //ç»„åˆçš„å’Œè¶…å‡ºç›®æ ‡å€¼ï¼Œè¿”å›
        if (cursum > tarsum)
            return;
        //1.æ»¡è¶³æ¡ä»¶ï¼Œå­˜å‚¨ç»“æœï¼Œè¿”å›
        if (path.size() == maxDepth && tarsum == cursum)
        {
            result.push_back(path);
            return;
        }
        for (int i = startindex; i <= 9 - (maxDepth - path.size()) + 1; i++)
        {   //2.é€’å½’éå†
            path.push_back(i);
            cursum += i;
            backtracking(tarsum, cursum, i + 1);
            //3.å›æº¯
            cursum -= i;
            path.pop_back();
        }
    }
};
